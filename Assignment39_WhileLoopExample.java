package assignments;

// Assignment 39 WAP to implement while loop
public class Assignment39_WhileLoopExample
{
	
	public static void main(String[] args) 
	{
		System.out.println("Print Alphabaets using While Loop !");
		
		char i = 'A';  										// Initialization

        while (i <= 'Z') 
        {  													// Condition
            System.out.print(" "+i);
            i++;  											// Increment
        }
	}
}
